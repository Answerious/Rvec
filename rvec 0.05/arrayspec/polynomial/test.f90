      PROGRAM M66INV_MAIN

      IMPLICIT NONE

      INTEGER :: I, J
      DOUBLE PRECISION, DIMENSION(6,6) :: MAT, MATINV
      LOGICAL :: OK_FLAG

      LOGICAL :: M66INV

!-----------------------------------------------------------------------------------------------------------------------------------

!
!     Get user input.
!

      WRITE (UNIT=*, FMT='(/A/)') ' Enter matrix:'

      DO I = 1, 6
         DO J = 1, 6
            WRITE (UNIT=*, FMT='(A,I1,1H,,I1,A)', ADVANCE='NO') ' A(', I, J, ') = '
            READ (UNIT=*, FMT=*) MAT(I,J)
         END DO
      END DO

!
!     Invert the input matrix.
!

      CALL M66INV (MAT, MATINV, OK_FLAG)

!
!     Print the result.
!

      IF (OK_FLAG) THEN
         WRITE (UNIT=*, FMT='(/A/)') ' Inverse:'
         WRITE (UNIT=*, FMT='(6ES25.15)') ((MATINV(I,J), J=1,6), I=1,6)
      ELSE
         WRITE (UNIT=*, FMT='(/A)') ' Singular matrix.'
      END IF

      STOP

      END PROGRAM M66INV_MAIN



